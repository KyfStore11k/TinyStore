package com.kyfstore.tinystore.api.v2;

/**
 * Extended value types supported by TinyStore API v2.
 */
public enum ValueType {
    /** String type. */
    STRING,

    /** Integer type. */
    INTEGER,

    /** Floating-point number type. */
    FLOAT,

    /** Boolean type. */
    BOOLEAN,

    /** Generic object type. */
    OBJECT,

    /** List/array type. */
    LIST,

    /** Map/dictionary type. */
    MAP
}
