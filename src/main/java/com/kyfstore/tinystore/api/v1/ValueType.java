package com.kyfstore.tinystore.api.v1;

/**
 * Enumeration of supported value types stored in TinyStore.
 */
public enum ValueType {
    /** String type. */
    STRING,

    /** Integer type. */
    INTEGER,

    /** Floating-point number type. */
    FLOAT,

    /** Boolean type. */
    BOOLEAN,

    /** Generic object type. */
    OBJECT
}
